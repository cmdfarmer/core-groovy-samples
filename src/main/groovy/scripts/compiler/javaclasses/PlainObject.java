package com.example.scripts.compiler.javaclasses;

public class PlainObject {
    private String name;
    private int id;

    public PlainObject(String name, int id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public int getId() {
        return id;
    }
}
